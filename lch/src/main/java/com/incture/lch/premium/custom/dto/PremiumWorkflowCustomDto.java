package com.incture.lch.premium.custom.dto;

public class PremiumWorkflowCustomDto {
	private String adhocOrderId;
	private String createdBy;
	private String createdDate;
	private String role;
	public String getAdhocOrderId() {
		return adhocOrderId;
	}
	public void setAdhocOrderId(String adhocOrderId) {
		this.adhocOrderId = adhocOrderId;
	}
	public String getCreatedBy() {
		return createdBy;
	}
	public void setCreatedBy(String createdBy) {
		this.createdBy = createdBy;
	}
	public String getCreatedDate() {
		return createdDate;
	}
	public void setCreatedDate(String createdDate) {
		this.createdDate = createdDate;
	}
	public String getRole() {
		return role;
	}
	public void setRole(String role) {
		this.role = role;
	}

}
